package programation.personal.redoexam;

public class Equipment {

    private String name;
    private int power;

    public Equipment() {
    }

    public Equipment(String name, int power) {
        this.name = name;
        this.power = power;
    }

    public String getName() {
        return name;
    }

    public int getPower() {
        return power;
    }
    

}
